<h2>IMAX</h2>

<div id="tout">
	<div id="bloc">
		<p class="para" id="chapo">Plus grand, plus beau et toujours plus impressionnant, le format de pellicule IMAX envahit les salles de cinéma mais <span class="gras">qu’est-ce que l’IMAX exactement ?</span></p>
		<img class="image" src="img/article/imax.jpg">
		<p class="para">IMAX pour Image Maximum, est un format de pellicule créé par l’IMAX Corporation, à Montréal au Canada et développé par Graeme Ferguson, Roman Kroitor, Robert Kerr et William C. Shaw. Ces écrans possèdent la capacité de diffuser des images d’une plus grande taille et d’une meilleure résolution que les pellicules conventionnelles.</p>
		<p class="para">Les salles équipées de cette technologie possèdent un écran d’une taille standard de 22 mètres de long et 16 mètres de haut mais ils peuvent être plus grand, le champion du domaine se situe à Sydney avec une dimension de 35,73 sur 29,42 mètres.</p>
		<p class="para">La projection IMAX présente des avantages certains aux salles conventionnelles comme une qualité d’image plus nette un son numérique réglé par système laser et une immersion totale dans les films. Pour renforcer cet effet, les fauteuils des salles Imax
sont disposés sur un plan très incliné, qui rapproche tous les rangs de l'écran.</p>
		<img class="image" src="img/article/imax_3.jpg">
		<p class="para">Le système IMAX numérique projette deux images afin de reproduire la vision des deux yeux. Deux projecteurs sont alors installés dans les cinémas équipés. Comme pour la projection 3D, les spectateurs portent des lunettes spéciales.</p>
		<p class="para">Cependant l’investissement des salles pour l’achat de cet équipement nécessite aux spectateurs de débourser en fonction des cinémas, 2 à 3€ supplémentaires.</p>
		<p class="para">En 2009, il existait 403 salles IMAX dans 44 pays différents. En France, il existe une quinzaine de ces écrans dans une dizaine de villes comme celle de La Géode à Paris ou encore la salle Pathé la Valette de Toulon.</p>
		<img class="image" src="img/article/imax_2.jpg">
		<p class="para">L’IMAX possède aussi une déclinaison appelée IMAX DOME ou OMNIMAX qui projette des films sur des écrans inclinés, et en relief, grâce à l’IMAX 3D.</p>
		<p class="para">A l’heure actuelle, de plus en plus de film sont tournés avec des caméras IMAX afin de pouvoir les diffuser dans ces salles sur mesure. Des films comme Star Wars : le Réveil de la force, Hunger Games : L’embrasement et plus récemment l’excellent Avengers : Infinity War en 2018 et prochainement la suite très attendu Avengers 4 : Endgame en 2019.</p>
